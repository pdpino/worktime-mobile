const DEFAULT_ICON_CONFIG = {
  name: 'image-search',
  type: 'material-community',
  size: 32,
};

const iconsConfigByName = {};

const iconsList = Object.keys(iconsConfigByCategory).map((categoryName) => {
  const configs = iconsConfigByCategory[categoryName];
  return {
    name: categoryName,
    icons: Object.keys(configs).map((iconName) => {
      const config = configs[iconName];
      // NOTE: populate right away the iconsConfigByName object,
      // to avoid iterating again.
      iconsConfigByName[iconName] = config;
      return {
        name: iconName,
        config,
      };
    }),
  };
});

export {
  iconsList,
  iconsConfigByName,
  DEFAULT_ICON_CONFIG,
};
